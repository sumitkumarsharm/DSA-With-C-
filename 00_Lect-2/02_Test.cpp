#include <iostream>
using namespace std;
int main()
{
    int a = 10, b = 5;
    cout << "sum = " << (a + b) << endl;
    cout << "difference = " << (a - b) << endl;
    cout << "Product = " << (a * b) << endl;
    cout << "Division = " << (a / b) << endl;
    cout << "modulo = " << (a % b) << endl;
    return 0;
}